    private void autonumber() {
       try {
           String sql = "SELECT MAX(id_supp) AS ID FROM supplier";
           java.sql.Connection conn = (Connection)connect_db.configDB();
           java.sql.Statement pst = conn.createStatement();
           java.sql.ResultSet rs = pst.executeQuery(sql);
           while(rs.next()) {
               Object[] obj = new Object[1];
               obj[0] = rs.getString("ID");
               if(obj[0] == null) {
                   obj[0] = "SP00";
               }
               String str_id = (String) obj[0];
               String kd = str_id.substring(2, 5);
               int int_code = Integer.parseInt(kd);
               int_code++;
               String a = String.format("%03d", int_code);
               String b = "SP" + a;
               txtidSupplier.setText(b);
               txtidSupplier.setEditable(false);
                }
           } catch (NumberFormatException | SQLException e) {      
       }
    }

