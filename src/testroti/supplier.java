    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int pop = JOptionPane.showConfirmDialog(this, "Yakin kah dek?", "Hapus", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (pop == JOptionPane.YES_OPTION) {                
            
        try {
            String sql = "DELETE FROM supplier WHERE id_supp = '"+txtidSupplier.getText()+"'";
            java.sql.Connection conn = (Connection)connect_db.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int row = jTable1.getSelectedRow();
            model.removeRow(row);
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
        load_table();
        kosong();
        // TODO add your handling code here:
    }//GEN-LAST:event_btnHapusActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int baris = jTable1.rowAtPoint(evt.getPoint());
        String kode_barang = jTable1.getValueAt(baris, 1).toString();
        txtidSupplier.setText(kode_barang);
        
        if (jTable1.getValueAt(baris, 2)==null) {
            txtnamasupp.setText("");
        } else {
            txtnamasupp.setText(jTable1.getValueAt(baris, 2).toString());
        }
        if (jTable1.getValueAt(baris, 3)==null) {
            txttelepon.setText("");
        } else {
            txttelepon.setText(jTable1.getValueAt(baris, 3).toString());
        }
        if (jTable1.getValueAt(baris, 4)==null) {
            txtalamat.setText("");
        } else {
            txtalamat.setText(jTable1.getValueAt(baris, 4).toString());
        }

    }//GEN-LAST:event_jTable1MouseClicked

